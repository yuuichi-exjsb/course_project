public class Unicode_print {
  public static void main(String[] args) {
    char nishi = '西';
    char da = '田';
    char yuu = '裕';
    char ichi = '一';
    System.out.println("西 = "+(int)nishi); 
    System.out.println("田 = "+(int)da); 
    System.out.println("裕 = "+(int)yuu); 
    System.out.println("一 = "+(int)ichi); 
  }
}
